// Bagaimana cara menambahkan sebuah array multidimensi ke dalam array menggunakan push()?
// Kunci Jawaban: Array tersebut akan menjadi elemen terakhir dari array utama.

// Advanced - Soal 2

// Apa yang akan terjadi jika kita memanggil push() pada sebuah array dengan array multidimensi sebagai argumen?
// Kunci Jawaban: Array multidimensi akan dimasukkan sebagai elemen tunggal ke dalam array.

// Advanced - Soal 3

// Jika kita memiliki array let arr = ['a', 'b'];, apa yang terjadi jika kita memanggil push() dengan arr sebagai argumen?
// Kunci Jawaban: Array arr akan dimasukkan sebagai elemen tunggal terakhir dari array utama.

// Advanced - Soal 4

// Bagaimana cara menambahkan objek ke dalam sebuah array menggunakan push() jika objek tersebut memiliki properti yang diperlukan?
// Kunci Jawaban: Objek akan dimasukkan sebagai elemen terakhir dari array.

// Advanced - Soal 5

// Jika kita memiliki array let arr = [1, 2];, apa yang terjadi jika kita memanggil push() dengan null sebagai argumen?
// Kunci Jawaban: Array akan memiliki satu elemen dengan nilai null.

// Advanced - Soal 6

// Bagaimana cara menambahkan beberapa array ke dalam array menggunakan push()?
// Kunci Jawaban: Memanggil push() dengan setiap array sebagai argumen, misalnya array.push(arr1, arr2).

// Advanced - Soal 7

// Jika kita memanggil push() pada sebuah array dengan sebuah string kosong sebagai argumen, apa yang terjadi?
// Kunci Jawaban: String kosong tersebut akan menjadi elemen tunggal dari array.

// Advanced - Soal 8

// Apa yang terjadi jika kita memanggil push() pada sebuah array dengan nilai true sebagai argumen?
// Kunci Jawaban: Array akan memiliki satu elemen dengan nilai true.

// Advanced - Soal 9

// Jika kita memanggil push() pada sebuah array dengan sebuah array kosong sebagai argumen, apa yang terjadi?
// Kunci Jawaban: Tidak ada perubahan pada array yang sudah ada.

// Advanced - Soal 10

// Bagaimana cara menambahkan elemen-elemen dari sebuah array ke dalam array lain menggunakan push() tanpa membuat array baru?
// Kunci Jawaban: Memanggil push() dengan spread operator (...) pada elemen-elemen yang ingin ditambahkan, misalnya array.push(...items).
