// Soal:
// Berikan array fruits = ["Apel", "Pisang", "Jeruk", "Mangga"]. Gunakan metode slice() untuk mendapatkan elemen dari indeks 1 hingga 3 (tidak termasuk indeks 3).
const fruits = ["Apel", "Pisang", "Jeruk", "Mangga"]
let result1 = fruits.slice(1, 3);
console.log("Hasil dari soal no 1 adalah = ")
console.log( result1)
// Jawaban:
// ["Pisang", "Jeruk"]

