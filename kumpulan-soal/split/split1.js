// Beginner
// Soal:
// Berikan string text = "Hello,World". Gunakan metode split() untuk membagi string menjadi array berdasarkan pemisah koma.
const text = "Hello,World";
const Arr1 = text.split(",");
console.log(Arr1);

// Jawaban:
// ["Hello", "World"]

